const DICTS: Record<Lang, Record<string, string>> = { EN: {}, FR }

export function translate(en: string, lang: Lang): string {
  if (lang === 'EN') return en
  return DICTS[lang][en] ?? en
}

/** Reads the current UI language from the logged-in user's school (EN default). */
export function useLang(): Lang {
  const lang = useAuthStore((s) => s.school?.language)
  return lang === 'FR' ? 'FR' : 'EN'
}

/** BCP-47 locale for Intl/toLocaleDateString, based on the school language. */
export function useLocaleCode(): string {
  return useLang() === 'FR' ? 'fr-FR' : 'en-US'
}

/** Returns a t(en) translator bound to the current school language. */
export function useT() {
  const lang = useLang()
  return (en: string) => translate(en, lang)
}
